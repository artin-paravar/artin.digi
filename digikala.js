const slideimage = () => {
  imagesindex =
    imagesindex === images.length
      ? 0
      : imagesindex < 0
      ? images.length - 1
      : imagesindex;
  carousel.style.transform = `translate(-${imagesindex * 100}%)`;
};
const updateClick = (e) => {
  clearInterval(intervalid);
  imagesindex += e.target.id === "next" ? 1 : -1;

  console.log(imagesindex);
  autoimage();
  slideimage(imagesindex);
};
button.forEach((button) => button.addEventListener("click", updateClick));

